#include <stdlib.h>

char	*ft_strdup(const char *s)
{
	int		i;
	int		size;
	char	*dest;

	size = 0;
	while (s[size])
		size++;
	if ((dest = malloc(sizeof(char) * size)) == NULL)
		return (NULL);
	i = 0;
	while (s[i])
	{
		dest[i] = s[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
